    private void cmdSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSaveActionPerformed
        // TODO add your handling code here:
        Boolean validation = false;
        try {
            SG.setSTICKY_PACKAGE(Double.parseDouble(txtStickyPackage.getText()));
            SG.setMEDIUM_PACKAGE(Double.parseDouble(txtMediumPackage.getText()));
            SG.setLARGE_PACKAGE(Double.parseDouble(txtLargePackage.getText()));
            validation= true;
        } catch (Exception e) {
            System.out.println("Error en Almacenar");
        }
        
        if (validation) {
            JOptionPane.showMessageDialog(null, "Datos actualizados");
        }

    }//GEN-LAST:event_cmdSaveActionPerformed

